// TestBuildWriteHealthSection asserts the report renders each write-health state
// distinctly (SPEC §14): healthy, below-floor, repositions, TapeAlert, and the
// "not measured" fallback when a tape carries no measurement.
func TestBuildWriteHealthSection(t *testing.T) {
	t.Parallel()

	manifest := func(health *report.WriteHealth) report.Manifest {
		m := completeManifest()
		m.Tapes = []report.Tape{{Barcode: "TAPE9999L6", Contents: []string{"zfs-media"}, WriteHealth: health}}

		return m
	}

	tests := []struct {
		name     string
		health   *report.WriteHealth
		contains []string
	}{
		{
			name:     "healthy",
			health:   &report.WriteHealth{ThroughputMBps: 160.0, FloorMBps: 50, FloorKnown: true, Healthy: true},
			contains: []string{"160.0", "healthy"},
		},
		{
			name:     "below floor",
			health:   &report.WriteHealth{ThroughputMBps: 42.0, FloorMBps: 50, FloorKnown: true, BelowFloor: true},
			contains: []string{"42.0", "below floor"},
		},
		{
			name:     "repositions",
			health:   &report.WriteHealth{ThroughputMBps: 120.0, FloorMBps: 50, FloorKnown: true, Repositions: 12},
			contains: []string{"12 repositions"},
		},
		{
			name:     "tape alert",
			health:   &report.WriteHealth{ThroughputMBps: 120.0, FloorMBps: 50, FloorKnown: true, TapeAlertFlags: []string{"20: Clean now"}},
			contains: []string{"TapeAlert", "Clean now"},
		},
		{
			name:     "floor unknown",
			health:   &report.WriteHealth{ThroughputMBps: 90.0, FloorKnown: false},
			contains: []string{"90.0", "floor unknown"},
		},
		{
			name:     "not measured",
			health:   nil,
			contains: []string{"not measured"},
		},
	}

	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			t.Parallel()

			extracted := stripWhitespace(extractText(t, manifest(test.health)))

			for _, want := range test.contains {
				assert.Contains(t, extracted, stripWhitespace(want),
					"write-health %q state should render %q", test.name, want)
			}
		})
	}
}

func TestBuildProducesValidPDF(t *testing.T) {
	t.Parallel()

	var buf bytes.Buffer
	require.NoError(t, report.Build(completeManifest(), &buf))

	assert.True(t, bytes.HasPrefix(buf.Bytes(), []byte("%PDF-")),
		"output does not start with the PDF magic header")
}
